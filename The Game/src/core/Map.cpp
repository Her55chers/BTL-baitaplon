Map::Map()
{
    sky = Texture::loadTexture("res/graphic/back_ground.png");
    ground = Texture::loadTexture("res/graphic/ground.png");
    grass = Texture::loadTexture("res/graphic/grass.png");
    //tree = Texture::loadTexture("res/graphic/tree.png");

    LoadMap(stage);

    src.x = src.y = 0;
    src.w = dst.w = 40;
    src.h = dst.h = 40;
    dst.x = dst.y = 0;
}

void Map::LoadMap(int arr[18][32])
{
    for(int row = 0; row < 18; row++){
        for(int col = 0; col < 32; col++){
            map[row][col] = arr[row][col];
        }
    }
}


void Map::DrawMap()
{
    int type = 0;

    for(int row = 0; row < 18; row++){
        for(int col = 0; col < 32; col++){
            type = map[row][col];

            dst.x = col * 40;
            dst.y = row * 40;

            switch(type)
            {
                case 0:
                    Texture::Draw(sky, src, dst);
                    break;
                case 1:
                    Texture::Draw(ground, src, dst);
                    break;
                case 2:
                    Texture::Draw(grass, src, dst);
                    break;
                /*case 3:
                    Texture::Draw(tree, src, dst);
                    break;*/
                default:
                    break;
            }

        }
    }
}
